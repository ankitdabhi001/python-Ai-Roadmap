no1=float(input("enter your first number : "))
no2=float(input("enter your second number : "))
op=input("enter your operater like(+,-,*)")

if op=="+":
    print(f"your sum is {no1+no2}")
elif op=="-":
     print(f"your sub is {no1-no2}")
elif op=="*":
     print(f"your mul is {no1*no2}")
elif op=="/":
     print(f"your div is {no1/no2}")